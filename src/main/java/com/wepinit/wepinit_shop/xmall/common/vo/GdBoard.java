package com.wepinit.wepinit_shop.xmall.common.vo;

public class GdBoard {

		private int sno;
		private String id;
		public int getSno() {
			return sno;
		}
		public void setSno(int sno) {
			this.sno = sno;
		}
		public String getId() {
			return id;
		}
		public void setId(String id) {
			this.id = id;
		}
		@Override
		public String toString() {
			return "GdBoard [sno=" + sno + ", id=" + id + "]";
		}
		
		
		
}
